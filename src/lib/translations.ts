export function t(key: string, language: string = 'en'): string {
  const langKey = language?.toLowerCase().slice(0, 2) || 'en';

  if (key === 'seven_day_forecast') {
    const raw10 = t('ten_day_forecast', language);
    let val = raw10;
    if (langKey === 'bn') {
      val = raw10.replace('১০', '৭');
    } else {
      val = raw10.replace('10', '7');
    }
    if (langKey === 'en') {
      val = val.replace('7-Day Forecast', '7 Day Forecast');
    }
    return val;
  }

  if (key === 'Unhealthy for Sensitive Groups') {
    if (langKey === 'en') return 'Unhealthy for Sensitive Groups';
    const val = t('Unhealthy for Sensitive', language);
    return val === 'Unhealthy for Sensitive' ? 'Unhealthy for Sensitive Groups' : val;
  }

  if (pollutantTranslations[langKey]?.[key]) {
    return pollutantTranslations[langKey][key];
  }

  // Dynamic regex-based parsing to support "Chance: DD%" instantaneously without latency
  if (key && key.startsWith("Chance:")) {
    const probabilityPart = key.replace("Chance:", "").trim();
    const translatedChance = customZeroLatencyTranslations[langKey]?.["Chance"] 
      || systemTranslations[langKey]?.["Chance"] 
      || "Chance";
    return `${translatedChance}: ${probabilityPart}`;
  }

  // Support "XH AGO" dynamically
  if (key && key.endsWith("H AGO")) {
    const hoursPart = key.replace("H AGO", "").trim();
    const translatedAgo = aqiUvTranslations[langKey]?.[ "H AGO" ] || "H AGO";
    return `${hoursPart}${translatedAgo}`;
  }

  if (aqiUvTranslations[langKey]?.[key]) {
    return aqiUvTranslations[langKey][key];
  }

  if (extraTranslations[langKey]?.[key]) {
    return extraTranslations[langKey][key];
  }
  if (extraTranslations[langKey]?.[key.toUpperCase()]) {
    return extraTranslations[langKey][key.toUpperCase()];
  }
  
  // Try custom zero latency translations first
  if (customZeroLatencyTranslations[langKey]?.[key]) {
    return customZeroLatencyTranslations[langKey][key];
  }
  if (customZeroLatencyTranslations.en[key]) {
    return customZeroLatencyTranslations.en[key];
  }

  // Try system translations first
  if (systemTranslations[langKey]?.[key]) {
    return systemTranslations[langKey][key];
  }
  if (systemTranslations.en[key]) {
    return systemTranslations.en[key];
  }

  const dict = dictionary[langKey] || dictionary.en;
  return dict[key] || dictionary.en[key] || key;
}

