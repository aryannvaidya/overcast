// Live custom reactive cache for dynamic loaded translations
const translationCache: Record<string, string> = (() => {
  if (typeof window === 'undefined') return {};
  try {
    const raw = localStorage.getItem('nimbus_translations_cache_v1');
    return raw ? JSON.parse(raw) : {};
  } catch {
    return {};
  }
})();

function saveTranslationToCache(key: string, val: string) {
  if (typeof window === 'undefined') return;
  try {
    translationCache[key] = val;
    localStorage.setItem('nimbus_translations_cache_v1', JSON.stringify(translationCache));
  } catch (err) {
    console.warn("localStorage quota exceeded or failed to save translation:", err);
  }
}

// Simple event-emitter to notify components when something is translated
type Listener = () => void;
const listeners = new Set<Listener>();
function emitTranslationChange() {
  listeners.forEach(l => l());
}

interface QueueItem {
  text: string;
  resolve: (translated: string) => void;
  reject: (err: any) => void;
}

const batchQueues: Record<string, QueueItem[]> = {};
const batchTimers: Record<string, any> = {};

async function flushQueue(langKey: string) {
  const queue = batchQueues[langKey] || [];
  batchQueues[langKey] = [];
  batchTimers[langKey] = null;
  
  if (queue.length === 0) return;
  
  // Dedup texts to fetch
  const uniqueTexts = Array.from(new Set(queue.map(item => item.text)));
  
  try {
    const res = await fetch('/api/translate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text: uniqueTexts, targetLanguage: langKey })
    });
    
    if (!res.ok) {
      throw new Error(`Server returned HTTP ${res.status}`);
    }
    
    const data = await res.json();
    const translatedArray = data?.translated;
    const mapping: Record<string, string> = {};
    
    if (Array.isArray(translatedArray)) {
      uniqueTexts.forEach((originalText, idx) => {
        const translatedVal = translatedArray[idx] || originalText;
        mapping[originalText] = translatedVal;
        
        // Save to translation cache
        const cacheKey = `${langKey}:${originalText}`;
        saveTranslationToCache(cacheKey, translatedVal);
      });
    } else if (typeof translatedArray === 'string' && uniqueTexts.length === 1) {
      const originalText = uniqueTexts[0];
      mapping[originalText] = translatedArray;
      const cacheKey = `${langKey}:${originalText}`;
      saveTranslationToCache(cacheKey, translatedArray);
    } else {
      // Fallback
      uniqueTexts.forEach(originalText => {
        mapping[originalText] = originalText;
      });
    }
    
    // Resolve all promises in the original queue with the mapped translations
    queue.forEach(item => {
      const translatedVal = mapping[item.text] || item.text;
      item.resolve(translatedVal);
    });
    
    emitTranslationChange();
    
  } catch (err) {
    // Resolve all queue items in the batch with original text on failure to recover gracefully without crashing
    queue.forEach(item => {
      item.resolve(item.text);
    });
  }
}

export function fetchDynamicTranslation(text: string, targetLanguage: string): Promise<string> {
  const langKey = (targetLanguage || 'en').toLowerCase().slice(0, 2);
  if (langKey === 'en' || !text) return Promise.resolve(text);
  
  const cacheKey = `${langKey}:${text}`;
  if (translationCache[cacheKey]) {
    return Promise.resolve(translationCache[cacheKey]);
  }

  // If the text is very long (e.g., Privacy Policy or Terms and Conditions markdown),
  // process it separately rather than in the general batch to keep responses snappy
  if (text.length > 300) {
    const controller = new AbortController();
    const timerId = setTimeout(() => {
      controller.abort();
    }, 4000); // 4-second timeout limit to guarantee responsiveness

    return fetch('/api/translate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ text, targetLanguage: langKey }),
      signal: controller.signal
    })
      .then(r => {
        clearTimeout(timerId);
        if (!r.ok) throw new Error(`HTTP ${r.status}`);
        return r.json();
      })
      .then(data => {
        const translated = data.translated || text;
        saveTranslationToCache(cacheKey, translated);
        emitTranslationChange();
        return translated;
      })
      .catch(err => {
        clearTimeout(timerId);
        return text; // Gracefully resolve with the English text so the app never hangs
      });
  }

  return new Promise<string>((resolve, reject) => {
    if (!batchQueues[langKey]) {
      batchQueues[langKey] = [];
    }
    
    batchQueues[langKey].push({ text, resolve, reject });
    
    if (batchTimers[langKey]) {
      clearTimeout(batchTimers[langKey]);
    }
    
    batchTimers[langKey] = setTimeout(() => {
      flushQueue(langKey);
    }, 60); // 60ms is optimal to collect all rendering component requests
  });
}

export function useTranslation(text: string, targetLanguage: string): { translated: string; loading: boolean } {
  const langKey = (targetLanguage || 'en').toLowerCase().slice(0, 2);
  const cacheKey = `${langKey}:${text}`;

  const getImmediateTranslation = () => {
    if (langKey === 'en' || !text) return text;
    const staticVal = t(text, langKey);
    if (staticVal !== text) return staticVal;
    return translationCache[cacheKey] || null;
  };

  const immediateVal = getImmediateTranslation();
  const [translated, setTranslated] = useState<string>(immediateVal || text);
  const [loading, setLoading] = useState<boolean>(langKey !== 'en' && !immediateVal);
  const [prevCacheKey, setPrevCacheKey] = useState<string>(cacheKey);

  if (cacheKey !== prevCacheKey) {
    setPrevCacheKey(cacheKey);
    setTranslated(immediateVal || text);
    setLoading(langKey !== 'en' && !immediateVal);
  }

  useEffect(() => {
    if (langKey === 'en' || !text) {
      setLoading(false);
      return;
    }
    
    const staticVal = t(text, langKey);
    if (staticVal !== text || translationCache[cacheKey]) {
      setLoading(false);
      return;
    }

    let active = true;
    setLoading(true);

    fetchDynamicTranslation(text, langKey).then(res => {
      if (active) {
        setTranslated(res);
        setLoading(false);
      }
    }).catch(() => {
      if (active) {
        setLoading(false);
      }
    });

    const handleUpdate = () => {
      if (active) {
        const val = translationCache[cacheKey];
        if (val) {
          setTranslated(val);
          setLoading(false);
        }
      }
    };
    listeners.add(handleUpdate);
    return () => {
      active = false;
      listeners.delete(handleUpdate);
    };
  }, [text, langKey, cacheKey]);

  return { 
    translated: cacheKey !== prevCacheKey ? (immediateVal || text) : translated, 
    loading: cacheKey !== prevCacheKey ? (langKey !== 'en' && !immediateVal) : loading 
  };
}

export function useTranslatedText(text: string, targetLanguage: string): string {
  const { translated } = useTranslation(text, targetLanguage);
  return translated;
}

import React from 'react';

export function Translate({ text, lang }: { text: string; lang: string }) {
  const translated = useTranslatedText(text, lang);
  return React.createElement(React.Fragment, null, translated);
}
